const mongoose = require('mongoose');
const userSchema = new mongoose.Schema({
    name: {
        type: String,
        required: true,
    },
    email: {
        type: String,
        required: true,
        unique: true,
    },
    password: {
        type: String,
        required: true,
    },
    profilePicture: {
        type: String,
    },
    interview_experience: {
        type: mongoose.Schema.Types.ObjectId,
        ref: 'InterviewExperience',
    },
    qna: {
        type: mongoose.Schema.Types.ObjectId,
        ref: 'Qna',
    },
    createdAt: {
        type: Date,
        default: Date.now,
    },
    updatedAt: {
        type: Date,
        default: Date.now,
    },
})

module.exports = mongoose.model('User', userSchema);